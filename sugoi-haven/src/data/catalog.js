/* ------------------------------------------------------------------ *
 * Listings — generated by scripts/fetch-inventory.mjs (runs before every
 * build). Source of truth: your Google Sheet when INVENTORY_CSV_URL is set,
 * otherwise scripts/demo-listings.json. Every real Listing must carry photos
 * of the exact physical print (blueprint §3b) before it goes live.
 * ------------------------------------------------------------------ */
import listingsJson from './listings.json';
export const listings = listingsJson;


/* ------------------------------------------------------------------ *
 * Helpers
 * ------------------------------------------------------------------ */
export const listingsForDesign = id => listings.filter(l => l.designId === id);
export const priceFrom = id => {
  const ls = listingsForDesign(id);
  return ls.length ? Math.min(...ls.map(l => l.price)) : null;
};
export const availableCount = id => listingsForDesign(id).length;
export const designsForSeries = s =>
  s.designIds.map(id => designs.find(d => d.id === id)).filter(Boolean);
export const listingsForSeries = s =>
  s.listingPublisher ? listings.filter(l => l.publisher === s.listingPublisher) : [];
export const publishers = [...new Set(listings.map(l => l.publisher))];
export const conditions = ['Fine', 'Good', 'Fair'];
